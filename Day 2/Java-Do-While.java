//Example Of DoWhile

class DoWhileExample {  
    public static void main(String[] args) {  
       int i=0;
        do{
        int j=1;
        while(j<=6)
        {
        System.out.print(j);
        j++;
        }
        System.out.println();
        i++;
        }
        while(i<=6); 
    }  
}
