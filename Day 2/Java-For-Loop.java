//Java Program to demonstrate the example of for loop  
//which prints table of 2  
public class ForExample {  
public static void main(String[] args) {  
    //Code of Java for loop  
    for(int i=1;i<=10;i++){
        int table =2*i;
        System.out.println("2"+"*"+i+"="+table);  
    }  
}  
}  
